export const minutesSeconds = (minutes: number): number => minutes * 60;
export const minutesMiliSeconds = (minutes: number): number =>
  minutes * 60 * 100;

export const daysSeconds = (days: number): number => days * 24 * 60 * 60;
export const daysMiliSeconds = (days: number): number => days * 24 * 60 * 1000;
